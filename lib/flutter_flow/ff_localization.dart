import 'package:flutter/material.dart';

String ffTranslate(
  BuildContext context,
  String source, {
  Map<String, String>? replacements,
}) {
  final locale = Localizations.maybeLocaleOf(context);
  final languageCode =
      (locale?.languageCode.isNotEmpty ?? false) ? locale!.languageCode : 'en';
  return ffTranslateForCode(
    languageCode,
    source,
    replacements: replacements,
  );
}

String ffTranslateForCode(
  String languageCode,
  String source, {
  Map<String, String>? replacements,
}) {
  final normalizedCode = languageCode.toLowerCase().split('_').first;
  final translated = _ffTranslations[normalizedCode]?[source] ?? source;
  if (replacements == null || replacements.isEmpty) {
    return translated;
  }

  var output = translated;
  replacements.forEach((key, value) {
    output = output.replaceAll('{$key}', value);
  });
  return output;
}

String ffLanguageName(BuildContext context, String languageCode) {
  switch (languageCode.toLowerCase()) {
    case 'en':
      return ffTranslate(context, 'English');
    case 'es':
      return ffTranslate(context, 'Spanish');
    case 'de':
      return ffTranslate(context, 'German');
    case 'fr':
      return ffTranslate(context, 'French');
    default:
      return languageCode.toUpperCase();
  }
}

